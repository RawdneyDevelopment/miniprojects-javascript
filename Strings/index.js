/* String, você pode concatenar ela*/

var nome = 'Rawdney'
var sobrenome = 'Mendes'
var nomeCompleto = nome +  ' ' + sobrenome
console.log(nomeCompleto);

/* Também pode somar string com números, nesse caso o número vai se transformar em uma string*/

var gols = 100
var frase = 'Romário fez ' + gols + ' gols'
console.log(typeof frase)


/* ele irá concatenar ambos, então sempre no final irá se transformar em uma string */
