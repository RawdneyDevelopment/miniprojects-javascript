/* Variáveis são responsáveis por guardar dados na memória
inicia com a palavra var, let ou const
*/

var nome = 'Rawdney'
let idade = 28
const possuiFaculdade = true;
console.log(`Meu nome é ${nome}, Minha idade: ${idade}, possuo faculdade? ${possuiFaculdade}`);
/* Você pode fazer operações básicas com variáveis*/
var preco = 25
var totalComprado = 5
var totalPreco = totalComprado * preco;
console.log(totalPreco);
/* Você simplesmente pode declarar variáveis assim:*/
var sobrenome = 'Mendes', 
cidade = 'Pernambuco';
console.log(sobrenome, cidade);

/* quando você somente declara a variável ela fica undefined*/
var valorIndefinido
console.log(valorIndefinido)



